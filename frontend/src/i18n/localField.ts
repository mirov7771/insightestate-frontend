export const localField = (field: fields) => {
  const locale = localStorage.getItem('language');
  let ln = LOCALES.RUSSIAN;

  if (locale) {
    ln = locale === 'en' ? LOCALES.ENGLISH : LOCALES.RUSSIAN;
  }
  return messages[ln][field];
};
